// DISCORD LOG WEBHOOK SİSTEMİ
function sendDiscordLog(actionMessage) {
    const webhookUrl = localStorage.getItem('tk_webhook_url');
    if (!webhookUrl || !webhookUrl.startsWith('https://discord.com/api/webhooks/')) return;

    fetch(webhookUrl, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
            username: "True Kinetic Bot",
            avatar_url: "https://images.unsplash.com/photo-1618005182384-a83a8bd57fbe?auto=format&fit=crop&w=150&q=80",
            embeds: [{
                title: "🔴 Sistem Panel Bildirimi / System Notification",
                description: actionMessage,
                color: 6514417, // Indigo Mavi #6366F1
                timestamp: new Date().toISOString(),
                footer: { text: "True Kinetic Hub Logs" }
            }]
        })
    }).catch(err => console.error("Webhook Error:", err));
}

