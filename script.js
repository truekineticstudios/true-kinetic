// TAKIM KADRO MODALI
function showTeam(index) {
    const t = translations[currentLang];
    const team = siteData.teams[index];
    const name = currentLang === 'tr' ? team.name_tr : team.name_en;
    const game = currentLang === 'tr' ? team.game_tr : team.game_en;

    document.getElementById('modal-team-name').innerText = name;
    document.getElementById('modal-team-game').innerText = game;

    const tbody = document.getElementById('modal-roster-body');
    tbody.innerHTML = "";
    if(team.roster.length === 0) {
        tbody.innerHTML = `<tr><td colspan='4'>${t.roster_empty_lbl}</td></tr>`;
    } else {
        team.roster.forEach(p => {
            const translatedRole = roleTranslations[currentLang][p.role] || p.role;
            tbody.innerHTML += `<tr>
                <td style="font-weight: 600; color: var(--text-bright);">${translatedRole}</td>
                <td style="color: var(--hover); font-weight: bold;">${p.nickname}</td>
                <td>${p.realname}</td>
                <td><span class="rank-badge">${p.rank}</span></td>
            </tr>`;
        });
    }
    openModal('teamModal');
}

// TURNUVA KURALLARI VE DETAY MODALI
function showTournamentRules(index) {
    const tour = siteData.tournaments[index];
    const name = currentLang === 'tr' ? tour.name_tr : tour.name_en;
    const start = currentLang === 'tr' ? tour.start_date_tr : tour.start_date_en;
    const end = currentLang === 'tr' ? tour.end_date_tr : tour.end_date_en;
    const prizes = currentLang === 'tr' ? tour.prizes_tr : tour.prizes_en;
    const rules = currentLang === 'tr' ? tour.rules_tr : tour.rules_en;

    document.getElementById('modal-tour-name').innerText = name;
    
    // Tarih ve Ödül metinlerini doldur
    document.getElementById('modal-tour-dates').innerText = `${start} - ${end}`;
    document.getElementById('modal-tour-prizes').innerText = prizes && prizes.trim() !== "" ? prizes : (currentLang === 'tr' ? "Duyurulacak." : "TBA");
    
    const rulesBody = document.getElementById('modal-tour-rules-body');
    rulesBody.innerText = rules && rules.trim() !== "" ? rules : (currentLang === 'tr' ? "Bu turnuva için özel bir kural belirtilmedi." : "No specific rules provided for this tournament.");
    
    // Her açıldığında onay kutucuğunu ve buton kilidini sıfırla
    document.getElementById('acceptTourRules').checked = false;
    document.getElementById('tourApplyBtn').style.opacity = "0.4";
    document.getElementById('tourApplyBtn').style.pointerEvents = "none";

    openModal('tournamentRulesModal');
}

